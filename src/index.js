const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

const morseNumbers = {
    '10': '.',
    '11': '-',
    '00': '',
    "**": ' ',
}


function decode(expr) {
    const charNumbers = expr.match(/(.{10})/g);

  return charNumbers.map(numbers => numbers.match(/(.{1,2})/g).map(number => morseNumbers[number]).join('')).map(word => MORSE_TABLE[word]).map(char => char ? char : " ").join(''); 
}

module.exports = {
    decode
}